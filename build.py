# ── parse one doc ──────────────────────────────────────────────────────────
def parse_doc(docx_path, approach_key):
    doc=Document(docx_path); abstract,num_to_abstract=build_numbering_lookup(doc); rels=build_rels(doc)
    body=doc.element.body; data={}

    # What is X? — free-flowing
    what_is=[]; in_wi=False
    for child in body:
        tag=child.tag.split('}')[-1]
        if tag=='p':
            from docx.text.paragraph import Paragraph as DP
            p=DP(child,doc); text=para_text(p); style=para_style(p); tl=text.lower()
            if not in_wi:
                if WHAT_IS_START(tl,style): in_wi=True; continue
            else:
                if WHAT_IS_END(tl,style): break
                if not text and not has_image(p): continue
                if has_image(p):
                    src=extract_image_b64(p,docx_path)
                    if src: what_is.append({'type':'image','src':src,'caption':re.sub(r'^\.\s*','',text).strip()})
                    continue
                if 'Caption' in style:
                    if what_is and what_is[-1].get('type')=='image' and not what_is[-1]['caption']:
                        what_is[-1]['caption']=re.sub(r'^\.\s*','',text).strip()
                    continue
                if 'Heading 3' in style:
                    what_is.append({'type':'subheading','text':para_html(p,rels),'is_html':True})
                    continue
                html_text=para_html(p,rels)
                if 'List Paragraph' in style:
                    level=get_bullet_level(p,abstract,num_to_abstract)
                    what_is.append({'type':'bullet','text':html_text,'tech':is_tech(p),'level':level,'is_html':True})
                else:
                    what_is.append({'type':'para','text':html_text,'is_html':True})
        elif tag=='tbl' and in_wi:
            from docx.table import Table as DT
            tbl=DT(child,doc); ttype=identify_table(tbl)
            if ttype=='ibl_component':
                items=[{'type':'bullet','text':f'{clean(r.cells[0].text)}. {clean(r.cells[1].text)}','tech':False,'level':0}
                       for r in tbl.rows[1:] if clean(r.cells[0].text) and clean(r.cells[1].text)]
                what_is.append({'type':'bullet_group','items':items})
    data['what_is']=what_is

    # Key question
    for tbl in doc.tables:
        if tbl.rows and tbl.columns:
            text=clean(tbl.rows[0].cells[0].text)
            if text.lower().startswith('key question'):
                data['key_question_paras']=[clean(p.text) for p in tbl.rows[0].cells[0].paragraphs if clean(p.text)]
                break

    # LE section
    le_preamble=[]; phase_table_data=None; in_le=False; found_phase=False
    for child in body:
        tag=child.tag.split('}')[-1]
        if tag=='p':
            from docx.text.paragraph import Paragraph as DP
            p=DP(child,doc); text=para_text(p); style=para_style(p); tl=text.lower()
            if not in_le:
                if LE_START(tl,style): in_le=True; continue
            else:
                if LE_END(tl,style): break
                if found_phase: continue
                if not text and not has_image(p): continue
                if has_image(p):
                    src=extract_image_b64(p,docx_path)
                    if src: le_preamble.append({'type':'image','src':src,'caption':re.sub(r'^\.\s*','',text).strip()})
                    continue
                if 'Heading 3' in style:
                    le_preamble.append({'type':'subheading','text':para_html(p,rels),'is_html':True})
                    continue
                html_text=para_html(p,rels)
                if 'List Paragraph' in style:
                    level=get_bullet_level(p,abstract,num_to_abstract)
                    le_preamble.append({'type':'bullet','text':strip_tech(html_text) if is_tech(p) else html_text,'tech':is_tech(p),'level':level,'is_html':True})
                else:
                    le_preamble.append({'type':'para','text':html_text,'is_html':True})
        elif tag=='tbl' and in_le:
            from docx.table import Table as DT
            tbl=DT(child,doc); ttype=identify_table(tbl)
            if ttype=='key_question': continue
            if ttype=='phase' and not found_phase:
                phase_table_data=parse_phase_table(tbl,abstract,num_to_abstract,rels); found_phase=True
    data['le_preamble']=le_preamble
    if phase_table_data: data['phase_table']=phase_table_data

    # Diff section — free-flowing stream (bullets, images, tables in document order)
    diff_stream=[]; in_diff=False; last_img_idx=None
    for child in body:
        tag=child.tag.split('}')[-1]
        if tag=='p':
            from docx.text.paragraph import Paragraph as DP
            p=DP(child,doc); text=para_text(p); style=para_style(p); tl=text.lower()
            if not in_diff:
                if DIFF_START(tl,style): in_diff=True; continue
            else:
                if has_image(p):
                    src=extract_image_b64(p,docx_path)
                    if src:
                        caption=re.sub(r'^\.\s*','',text).strip()
                        diff_stream.append({'type':'image','src':src,'caption':caption})
                        last_img_idx=len(diff_stream)-1
                    continue
                if 'Caption' in style and last_img_idx is not None:
                    if not diff_stream[last_img_idx]['caption']:
                        diff_stream[last_img_idx]['caption']=re.sub(r'^\.\s*','',text).strip()
                    continue
                if not text: continue
                if 'Heading 3' in style:
                    diff_stream.append({'type':'subheading','text':para_html(p,rels),'is_html':True})
                    continue
                html_text=para_html(p,rels)
                if 'List Paragraph' in style:
                    level=get_bullet_level(p,abstract,num_to_abstract)
                    diff_stream.append({'type':'bullet','text':strip_tech(html_text) if is_tech(p) else html_text,'tech':is_tech(p),'level':level,'is_html':True})
                else:
                    diff_stream.append({'type':'para','text':html_text,'is_html':True})
        elif tag=='tbl' and in_diff:
            from docx.table import Table as DT
            tbl=DT(child,doc); ttype=identify_table(tbl)
            if ttype=='diff_ibl':
                diff_stream.append({'type':'table','table_type':'diff_ibl','data':parse_diff_table_ibl(tbl)})
            elif ttype=='diff_standard':
                diff_stream.append({'type':'table','table_type':'diff_standard','data':parse_diff_table_standard(tbl,abstract,num_to_abstract,rels)})
    data['diff_stream']=diff_stream
    return data

