def compress_image(image_bytes, max_width=900, quality=72):
    try:
        img = Image.open(io.BytesIO(image_bytes))
        if img.mode in ('RGBA','P','LA'):
            bg = Image.new('RGB', img.size, (255,255,255))
            img2 = img.convert('RGBA') if img.mode=='P' else img
            bg.paste(img2, mask=img2.split()[-1])
            img = bg
        elif img.mode != 'RGB': img = img.convert('RGB')
        if img.width > max_width:
            ratio = max_width/img.width
            img = img.resize((max_width,int(img.height*ratio)), Image.LANCZOS)
        buf = io.BytesIO()
        img.save(buf, format='JPEG', quality=quality, optimize=True)
        return buf.getvalue(), 'image/jpeg'
    except: return image_bytes, ('image/png' if image_bytes[:4]==b'\x89PNG' else 'image/jpeg')

def extract_image_b64(p, docx_path):
    blips = p._element.findall('.//' + qn('a:blip'))
    if not blips: return None
    rId = blips[0].get(qn('r:embed'))
    if not rId: return None
    doc2 = Document(docx_path)
    for rel in doc2.part.rels.values():
        if rel.reltype.endswith('/image') and rel.rId == rId:
            image_bytes = rel.target_part.blob
            if len(image_bytes) > 100*1024:
                image_bytes, mime = compress_image(image_bytes)
            else:
                mime = 'image/png' if image_bytes[:4]==b'\x89PNG' else 'image/jpeg'
            b64 = base64.b64encode(image_bytes).decode('utf-8')
            return f'data:{mime};base64,{b64}'
    return None

# ── numbering lookup ───────────────────────────────────────────────────────
def build_numbering_lookup(doc):
    numbering_part = None
    for rel in doc.part.rels.values():
        if 'numbering' in rel.reltype: numbering_part = rel.target_part; break
    if not numbering_part: return {}, {}
    ns = 'http://schemas.openxmlformats.org/wordprocessingml/2006/main'
    root = etree.fromstring(numbering_part.blob)
    abstract = {}
    for abNum in root.findall(f'{{{ns}}}abstractNum'):
        abId = abNum.get(f'{{{ns}}}abstractNumId'); abstract[abId] = {}
        for lvl in abNum.findall(f'{{{ns}}}lvl'):
            ilvl = lvl.get(f'{{{ns}}}ilvl')
            lvlText = lvl.find(f'{{{ns}}}lvlText')
            abstract[abId][ilvl] = lvlText.get(f'{{{ns}}}val','') if lvlText is not None else ''
    num_to_abstract = {}
    for num in root.findall(f'{{{ns}}}num'):
        numId = num.get(f'{{{ns}}}numId')
        abRef = num.find(f'{{{ns}}}abstractNumId')
        if abRef is not None: num_to_abstract[numId] = abRef.get(f'{{{ns}}}val')
    return abstract, num_to_abstract

def get_bullet_level(p, abstract, num_to_abstract):
    numPr = p._element.find('.//' + qn('w:numPr'))
    ilvl_val=0; numId_val=None
    if numPr is not None:
        ilvl_el=numPr.find(qn('w:ilvl')); numId_el=numPr.find(qn('w:numId'))
        if ilvl_el is not None: ilvl_val=int(ilvl_el.get(qn('w:val'),'0'))
        if numId_el is not None: numId_val=numId_el.get(qn('w:val'))
    if ilvl_val>=1: return 1
    if numId_val and numId_val in num_to_abstract:
        abId=num_to_abstract[numId_val]
        if abstract.get(abId,{}).get(str(ilvl_val),'')=='o': return 1
    return 0

def cell_bullets(cell, abstract, num_to_abstract, rels=None):
    bullets=[]
    for p in cell.paragraphs:
        t=para_text(p)
        if not t: continue
        tech=is_tech(p); level=get_bullet_level(p,abstract,num_to_abstract)
        html_text=para_html(p, rels)
        display=strip_tech(html_text) if tech else html_text
        bullets.append({'text':display,'tech':tech,'level':level,'is_html':True})
    return bullets

def cell_plain(cell): return clean(cell.text)

TAG_RE = re.compile(r'^\[([^\]]+)\]')
def parse_aims_cell(cell):
    paras=[clean(p.text) for p in cell.paragraphs if clean(p.text)]
    blocks=[]; current_tag=None; current_lines=[]
    def flush():
        if current_tag is not None: blocks.append({'tag':current_tag,'text':' '.join(current_lines).strip()})
    for para in paras:
        m=TAG_RE.match(para)
        if m:
            flush(); current_tag=m.group(1).strip(); rest=para[m.end():].strip()
            current_lines=[rest] if rest else []
        else:
            if current_tag is not None: current_lines.append(para)
            else: blocks.append({'tag':None,'text':para})
    flush()
    return blocks if blocks else None

def identify_table(tbl):
    if not tbl.rows: return 'unknown'
    first_cell=clean(tbl.rows[0].cells[0].text)
    if len(tbl.rows)==1 and len(tbl.columns)==1 and first_cell.lower().startswith('key question'): return 'key_question'
    headers=[clean(c.text).lower() for c in tbl.rows[0].cells]
    if len(tbl.columns)==2 and headers[0] in ('component',''): return 'ibl_component'
    if any('teacher' in h for h in headers) and any('student' in h for h in headers): return 'phase'
    if any('lower' in h for h in headers) and any('higher' in h for h in headers):
        return 'diff_ibl' if len(tbl.columns)>=6 else 'diff_standard'
    if len(tbl.columns)>=6 and any('more' in h or 'essential' in h for h in headers): return 'diff_ibl'
    return 'unknown'

def parse_phase_table(tbl, abstract, num_to_abstract, rels=None):
    rows=tbl.rows
    if not rows: return None
    col_headers=[cell_plain(c) for c in rows[0].cells]; n_cols=len(col_headers)
    teacher_col=next((i for i,h in enumerate(col_headers) if 'teacher' in h.lower()),1)
    student_col=next((i for i,h in enumerate(col_headers) if 'student' in h.lower()),2)
    aims_col=next((i for i,h in enumerate(col_headers) if any(k in h.lower() for k in ['align','aim','relev'])),n_cols-1 if n_cols>3 else None)
    parsed_rows=[]
    for row in rows[1:]:
        cells=row.cells
        if not cells or not any(cell_plain(c) for c in cells): continue
        # Phase label: Heading 3 = main name, Normal = sub-label
        col0_paras = [(p.style.name or '', para_html(p, rels), para_text(p))
                      for p in cells[0].paragraphs if para_text(p).strip()]
        if col0_paras:
            main = col0_paras[0]
            phase_label = re.sub(r'^\d+\.\s*','', (main[2] if not main[1] else main[2])).strip()
            phase_html_label = main[1] if main[1] else esc(main[2])
            sub_paras = col0_paras[1:]
            phase_sub = ' '.join(p[2] for p in sub_paras).strip()
            phase_sub_html = ' '.join(p[1] for p in sub_paras).strip()
        else:
            phase_label = ''; phase_html_label = ''; phase_sub = ''; phase_sub_html = ''
        teacher_bullets=cell_bullets(cells[teacher_col],abstract,num_to_abstract,rels) if teacher_col<len(cells) else []
        student_bullets=cell_bullets(cells[student_col],abstract,num_to_abstract,rels) if student_col<len(cells) else []
        aims_blocks=parse_aims_cell(cells[aims_col]) if aims_col is not None and aims_col<len(cells) else None
        if phase_label and not teacher_bullets and not student_bullets and not aims_blocks: continue
        parsed_rows.append({'phase':phase_label,'phase_html':phase_html_label,'phase_sub':phase_sub,'phase_sub_html':phase_sub_html,'teacher':teacher_bullets,'student':student_bullets,'aims':aims_blocks})
    return {'col_headers':col_headers,'teacher_col':teacher_col,'student_col':student_col,'aims_col':aims_col,'rows':parsed_rows}

def parse_diff_stage_cell(cell, rels=None):
    """Parse diff table stage column: Heading 3 = main label, Normal = sub-label."""
    paras = [(p.style.name or '', para_html(p, rels), para_text(p))
             for p in cell.paragraphs if para_text(p).strip()]
    if not paras: return '', '', '', ''
    main_html = paras[0][1]; main_plain = paras[0][2]
    sub_paras = paras[1:]
    sub_html = ' '.join(p[1] for p in sub_paras).strip()
    sub_plain = ' '.join(p[2] for p in sub_paras).strip()
    return main_plain, main_html, sub_plain, sub_html

def parse_diff_table_standard(tbl, abstract=None, num_to_abstract=None, rels=None):
    rows=tbl.rows
    if not rows: return {'type':'standard','headers':[],'stages':[],'has_role_col':False}
    headers=[cell_plain(c) for c in rows[0].cells]
    lower_col=next((i for i,h in enumerate(headers) if 'lower' in h.lower()),1)
    higher_col=next((i for i,h in enumerate(headers) if 'higher' in h.lower()),2)
    role_col=next((i for i,h in enumerate(headers) if h.lower()=='role'),None)
    stages=[]
    ab=abstract or {}; nm=num_to_abstract or {}
    for row in rows[1:]:
        cells=row.cells
        if not cells or not any(cell_plain(c) for c in cells): continue
        stage_plain, stage_html, stage_sub, stage_sub_html = parse_diff_stage_cell(cells[0], rels)
        lower_bullets=cell_bullets(cells[lower_col],ab,nm,rels) if lower_col<len(cells) else []
        higher_bullets=cell_bullets(cells[higher_col],ab,nm,rels) if higher_col<len(cells) else []
        role_val=cell_plain(cells[role_col]) if role_col is not None and role_col<len(cells) else None
        if role_col is not None:
            if stage_plain and (not stages or stage_plain!=stages[-1]['stage']):
                stages.append({'stage':stage_plain,'stage_html':stage_html,'stage_sub':stage_sub,'stage_sub_html':stage_sub_html,
                               'teacher_lower':[],'teacher_higher':[],'student_lower':[],'student_higher':[]})
            if stages:
                if role_val and 'teacher' in role_val.lower(): stages[-1]['teacher_lower']=lower_bullets; stages[-1]['teacher_higher']=higher_bullets
                elif role_val and 'student' in role_val.lower(): stages[-1]['student_lower']=lower_bullets; stages[-1]['student_higher']=higher_bullets
        else:
            if stage_plain:
                stages.append({'stage':stage_plain,'stage_html':stage_html,'stage_sub':stage_sub,'stage_sub_html':stage_sub_html,
                               'lower':lower_bullets,'higher':higher_bullets})
            elif stages:
                stages[-1]['lower']=stages[-1].get('lower',[])+lower_bullets
                stages[-1]['higher']=stages[-1].get('higher',[])+higher_bullets
            else:
                stages.append({'stage':UNNAMED,'stage_html':UNNAMED,'stage_sub':'','stage_sub_html':'',
                               'lower':lower_bullets,'higher':higher_bullets})
    return {'type':'standard','headers':headers,'stages':stages,'has_role_col':role_col is not None}

def parse_diff_table_ibl(tbl):
    rows=tbl.rows
    if not rows: return {'type':'ibl_matrix','features':[]}
    features=[]; seen={}
    for row in rows[2:]:
        cells=row.cells
        if len(cells)<6: continue
        fv=clean(cells[0].text); rv=clean(cells[1].text)
        lc=[clean(cells[i].text) for i in range(2,6)]
        if not fv and not rv: continue
        if fv and fv not in seen: seen[fv]=len(features); features.append({'name':fv,'teacher':[],'students':[]})
        fi=seen.get(fv,len(features)-1)
        if fi>=len(features): continue
        if 'teacher' in rv.lower(): features[fi]['teacher']=lc
        elif 'student' in rv.lower(): features[fi]['students']=lc
    return {'type':'ibl_matrix','features':features}

